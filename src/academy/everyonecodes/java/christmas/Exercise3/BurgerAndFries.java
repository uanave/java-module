package academy.everyonecodes.java.christmas.Exercise3;

public class BurgerAndFries {
    private String name;
    private double price;

    public BurgerAndFries(String name, double price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public double getPrice() {
        return price;
    }
}