package academy.everyonecodes.java.week2.set2.exercise5;

public class Shoe {
    private int size;
    private String color;

    public Shoe(int size, String color) {
        this.size = size;
        this.color = color;
    }

    public int getSize() {
        return size;
    }

    public String getColor() {
        return color;
    }
}

